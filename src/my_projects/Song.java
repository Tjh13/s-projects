package my_projects;

public class Song {

	// Data Members
	String name = "";
	String album = "";
	String artist = "";
	String year = "";
	
	// Parameterized constructor
	public Song(String n, String al, String ar, String y) {
		
		this.name = n;
		this.album = al;
		this.artist = ar;
		this.year = y;
		
	}

}
